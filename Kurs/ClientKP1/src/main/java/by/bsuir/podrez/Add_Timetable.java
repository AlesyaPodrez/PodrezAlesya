    public Add_Timetable(TimetableSettings timetable) {
        initForm();
        jTextArea1.setVisible(false);
        this.timetable = timetable;
        jComboBox1.setSelectedItem(tl.getNamePerformance(timetable.getId_performance()));
        jComboBox2.setSelectedItem(tl.getNameActor(timetable.getId_actors()));
        str = timetable.getDate_setting();
        parts = str.split(" ");
        jComboBox4.setSelectedItem(parts[0]);
        jComboBox3.setSelectedItem(parts[1]);
        jComboBox5.setSelectedItem(parts[2]);
        jTextField3.setText(timetable.getTime_ofthe());
    }
    
