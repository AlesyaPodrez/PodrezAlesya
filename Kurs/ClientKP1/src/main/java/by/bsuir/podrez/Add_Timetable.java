    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextArea1.getText().isEmpty()) {
            str = jComboBox4.getSelectedItem() + " " + jComboBox3.getSelectedItem() + " " + jComboBox5.getSelectedItem();
            this.timetable.setId_performance(tl.getPerformanceId((String) jComboBox1.getSelectedItem()));
            this.timetable.setId_actors(tl.getActorId((String) jComboBox2.getSelectedItem()));
            this.timetable.setDate_setting(str);
            this.timetable.setTime_ofthe(jTextField3.getText());
            tl.saveTimetable(timetable);
            dispose();
            Timetable_Form mainForm = new Timetable_Form();
            mainForm.setVisible(true);
        }
        else JOptionPane.showMessageDialog(null, "Не все поля заполнены.");
    }//GEN-LAST:event_jButton3ActionPerformed

